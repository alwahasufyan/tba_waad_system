import { useEffect, useState, useCallback } from 'react';
import visitsService from 'services/api/visits.service';

/**
 * Hook for fetching paginated visits list
 * @param {Object} initialParams - Initial query parameters
 * @returns {Object} { data, loading, error, params, setParams, refresh }
 */
export const useVisitsList = (initialParams = {}) => {
  const [params, setParams] = useState({
    page: 1,
    size: 20,
    sortBy: 'visitDate',
    sortDir: 'desc',
    search: '',
    ...initialParams
  });

  const [data, setData] = useState({
    items: [],
    total: 0,
    page: 1,
    size: 20
  });

  const [loading, setLoading] = useState(false);
  const [error, setError] = useState(null);

  const load = useCallback(async () => {
    try {
      setLoading(true);
      setError(null);
      const response = await visitsService.getAll(params);

      // Defensive: handle multiple response shapes
      const paginationData = response?.data?.items ? response.data : response;
      const items = Array.isArray(paginationData?.items) 
        ? paginationData.items 
        : Array.isArray(paginationData) 
          ? paginationData 
          : [];

      setData({
        items,
        total: paginationData?.total ?? paginationData?.totalElements ?? 0,
        page: paginationData?.page ?? params.page,
        size: paginationData?.size ?? params.size
      });
    } catch (err) {
      console.error('[useVisits] Failed to load visits list:', err);
      setError(err);
      setData({ items: [], total: 0, page: params.page, size: params.size });
    } finally {
      setLoading(false);
    }
  }, [params]);

  useEffect(() => {
    load();
  }, [load]);

  const refresh = useCallback(() => {
    load();
  }, [load]);

  return {
    data,
    loading,
    error,
    params,
    setParams,
    refresh
  };
};

/**
 * Hook for fetching single visit details
 * @param {number} id - Visit ID
 * @returns {Object} { data, loading, error, refresh }
 */
export const useVisitDetails = (id) => {
  const [data, setData] = useState(null);
  const [loading, setLoading] = useState(false);
  const [error, setError] = useState(null);

  const load = useCallback(async () => {
    if (!id) return;

    try {
      setLoading(true);
      setError(null);
      const response = await visitsService.getById(id);
      setData(response?.data || response);
    } catch (err) {
      console.error('[useVisits] Failed to load visit details:', err);
      setError(err);
      setData(null);
    } finally {
      setLoading(false);
    }
  }, [id]);

  useEffect(() => {
    load();
  }, [load]);

  const refresh = useCallback(() => {
    load();
  }, [load]);

  return {
    data,
    loading,
    error,
    refresh
  };
};

/**
 * Hook for fetching visits by member
 * @param {number} memberId - Member ID
 * @returns {Object} { data, loading, error, refresh }
 */
export const useVisitsByMember = (memberId) => {
  const [data, setData] = useState([]);
  const [loading, setLoading] = useState(false);
  const [error, setError] = useState(null);

  const load = useCallback(async () => {
    if (!memberId) return;

    try {
      setLoading(true);
      setError(null);
      const response = await visitsService.getByMember(memberId);
      setData(Array.isArray(response?.data) ? response.data : Array.isArray(response) ? response : []);
    } catch (err) {
      console.error('[useVisits] Failed to load member visits:', err);
      setError(err);
      setData([]);
    } finally {
      setLoading(false);
    }
  }, [memberId]);

  useEffect(() => {
    load();
  }, [load]);

  const refresh = useCallback(() => {
    load();
  }, [load]);

  return {
    data,
    loading,
    error,
    refresh
  };
};
